public class rightTriangleInverted {
    public static void main(String ar[]){
        int i = 5, j;
        System.out.println("Enter the number");
        for(i=5; i>=1;i--){
            for(j=1; j<=i;j++){
                System.out.print("*");
            }
            System.out.println("");
        }
    }
}
